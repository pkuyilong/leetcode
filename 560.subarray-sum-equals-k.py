class Solution:
    def subarraySum(self, nums, k) -> int:
        if len(nums) == 0:
            return 0
        for i in range(1, len(nums)):
            nums[i] = nums[i] + nums[i - 1]
        count = 0

        j = 0
        while j < len(nums):
            if nums[j] == k:
                count += 1
            j += 1

        for i in range(1, len(nums)):
            j = i
            while j < len(nums):
                if nums[j] - nums[i - 1] == k:
                    count += 1
                j += 1
        return count


